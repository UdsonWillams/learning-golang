// Utilizando o operador curto de declaração, atribua estes valores
// as variaveis com os identificadores: "x", "y" e "z"
// 1: 42, 2: "James Bond", 3: true
// Depois demonstre os valores nestas variaveis, com:
// 1. Uma unica declaração print.
// 2. Multiplas declarações print.
package main

import "fmt"

func main() {
	x := 42
	y := "James Bond"
	z := true
	fmt.Println(x, y, z)
	fmt.Println(x)
	fmt.Println(y)
	fmt.Println(z)
}

