// - Crie um programa que utilize a declaração switch,
// onde o switch statement seja uma variável do tipo
// string com identificador "esporteFavorito".

package main

import "fmt"

func main() {
	esporteFavorito := "mma"
	switch esporteFavorito {
	case "mma":
		fmt.Println("alexi poatann")
	case "boxe":
		fmt.Println("myke tyysown")
	case "e-sports":
		fmt.Println("bi erre teu teu")
	}
}
